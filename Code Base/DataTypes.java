public class DataTypes {
    public static void main(String[] args){
        //Small numbers
        byte b = 100; 
        //Medium numbers
        short c = 5000;
        //Large numbers
        int d = 100000;
        //Very large numbers
        Long e = 1500000000L;
        //Decimal numbers
        float f = 3.25f;
        //More precise decimal numbers(more exact)
        double g = 3.1415926535897932384626433832795d;
        //Scientific numbers
        float fi = 35e3f;
        double di = 12E4d;
        //Boolean values
        boolean isjavafun = true;
        boolean isFishtasty = false;
        //Character values
        char mygrade = 'A';
        //String Values
        String myname = "Ridhhima Rai";
        //Printing Details
        System.out.println("Byte: " + b);
        System.out.println("Short: " + c);
        System.out.println("Int: " + d);
        System.out.println("Long: " + e);
        System.out.println("Float: " + f);
        System.out.println("Double: " + g);
        System.out.println("Float (Scientific): " + fi);
        System.out.println("Double (Scientific): " + di);
        System.out.println("Boolean (Java is fun): " + isjavafun);
        System.out.println("Boolean (Fish is tasty): " + isFishtasty);
        System.out.println("Character: " + mygrade);
        System.out.println("String: " + myname);

    }
    
}
