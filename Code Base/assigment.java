public class assigment {
    public static void main (String [] args) {
        int x = 5;
        int y = 6;
        System.out.println("Assingement Operators");
        //Used to assing the value 
        System.out.println(x = 5);
        System.out.println(y = 6);
        //Used to add and subtract 
        System.out.println(x += 5);
        System.out.println(y -= 3);
        //Used to multiply and divide 
        System.out.println(x *= 2);
        System.out.println(y /= 2);
        //Used to find the remainder 
        System.out.println(x %= 3);
        System.out.println(y %= 2);
        //Used to perform bitwise AND, OR, and XOR 
        System.out.println(x &= 3);
        System.out.println(y &= 2);
        //Used to perform bitwise OR, left shift, right shift, and XOR 
        System.out.println(x |= 3);
        System.out.println(y |= 2);
        //Used to perform bitwise right shift, left shift, division, and XOR 
        System.out.println(x >>= 3);
        System.out.println(y <<= 2);
        //Used to perform bitwise division and XOR 
        System.out.println(x /= 3);
        System.out.println(y /= 2);
        //Used to perform bitwise XOR 
        System.out.println(x ^= 3);
        System.out.println(y ^= 2);






    }
    
}
